<?php

$swap = function (&$a, &$b) {
    $temp = $a; // Сохраняем значение $a
    $a = $b;    // Присваиваем $a значение $b
    $b = $temp;  // Присваиваем $b старое значение $a
};

$a = 5;
$b = 8;

echo "До обмена: a = $a, b = $b<br>"; // 5, 8

$swap($a, $b);

echo "После обмена: a = $a, b = $b<br>"; // 8, 5

?>